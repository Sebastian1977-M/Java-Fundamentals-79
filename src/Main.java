public class Main { // definirea clasei Main
    public static void main(String[] args) { // metoda main = punctul de intratrare in program = ce se ruleaza
        System.out.println("Hello world!"); // afisarea mesajului "Hello world!"
        // comentariu pe o singura linie = nu este rulat
        /*
        comentariu
        pe mai
        multe linii
         */

        // java este case-sensitive -> main != Main

        // sout - pentru a genera System.out.println();
        System.out.println("Hello, Java79!");
        System.out.print("Java");
        System.out.print(" is cool");
        // println printeaza mesajul + un rand nou la final
        // print printeaza mesajul, fara a adauga un rand nou

        System.out.println(); // printeaza un rand nou
        // \n - caracterul care reprezinta rand nou
        System.out.print("Java\n");
        System.out.print(" is cool");
    }
}